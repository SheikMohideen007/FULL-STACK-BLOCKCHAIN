//for celsius to fahrenheit
var num = parseInt(60);
var fahrenheit = (num * 9) / 5 + 32;
console.log(
  `${num} degree celsius is equal to ${fahrenheit} degree fahrenheit`
);

//for fahrenheit to celsius
var num = parseInt(45);
var celsius = ((num - 32) * 5) / 9;
console.log(`${num} degree fahrenheit is equal to ${celsius} degree celsius`);
